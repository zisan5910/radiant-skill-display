export const getProductsByCategory = (categoryId: string): ProductProps[] => {
  switch (categoryId.toLowerCase()) {
    case "fashion":
      return fashionProducts;
    case "electronics":
      return electronicsProducts;
    case "others":
      return otherProducts;
    default:
      return [];
  }
};

