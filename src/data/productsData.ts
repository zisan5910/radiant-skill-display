export const getProductById = (id: string): ProductProps | undefined => {
  const allProducts = [
    ...featuredProducts,
    ...fashionProducts.filter(p => !featuredProducts.some(fp => fp.id === p.id)),
    ...electronicsProducts.filter(p => !featuredProducts.some(fp => fp.id === p.id)),
    ...otherProducts.filter(p => !featuredProducts.some(fp => fp.id === p.id)),
  ];
  
  return allProducts.find(product => product.id === id);
};

